# 文件的概念和作用
# *计算机的 文件，就是存储在某种 长期储存设备 上的一段 数据
# * 长期存储设备包括：硬盘、U 盘、移动硬盘、光盘...

# 文件的作用
# 将数据长期保存下来，在需要的时候使用
# 文件的存储方式
# *在计算机中，文件是以 二进制 的方式保存在磁盘上的

# 文本文件和二进制文件
# *文本文件
#   *可以使用 文本编辑软件 查看
#   *本质上还是二进制文件
#   *例如：python 的源程序
# *二进制文件
#   *保存的内容 不是给人直接阅读的，而是 提供给其他软件使用的
#   *例如：图片文件、音频文件、视频文件等等
#   *二进制文件不能使用 文本编辑软件 查看