# 过程 是早期的一个编程概念
# 过程 类似于函数，只能执行，但是没有返回值
# 函数 不仅能执行，还可以返回结果

# 面向过程——怎么做？
# 1.把所有过程从头到尾逐步实现
# 2.将某些 功能独立 的代码 封装 成一个个 函数
# 3.最后代码就是 顺序的调用 不同的 函数
# *特点：
#   1. 注重 步骤与过程 ，不注重 职责分工
#   2.如果需求复杂，代码变得很 复杂
#   3.开发复杂的项目，没有固定套路，难度大

# 面向对象——谁来做？
# > 相比函数，面向对象 是 更大 的封装
# 1.在完成某一个需求前，首先指定 职责——做什么
# 2.根据 职责 确定 不同的 对象，在 对象 内部封装不同的 方法
# 3.最后完成的代码，就是顺序地让 不同的对象 调用 不同的方法
# *特点：
#   1.注重 对象和职责，不同的对象承担不同的职责
#   2.更加适合应对复杂的需求变化，是专门应对负载项目开发，提供的固定套路
#   3.需要在面向过程的基础上，在学习一些 面向对象的语法